class CStudent;     //向前声明

class COther        
{
public:
    void func(void);
    CStudent *CreateObject(void);
};
 
class CStudent
{
    //将普通函数声明为该类的友元
    friend void normal_func(void);

    //将他类成员函数声明为该类的友元
    friend void COther::func(void);
    friend CStudent * COther::CreateObject(void);

    //将它类声明为该类的友元, 至于该类是否存在，是否实现，都不进行检查
    friend class C;
    
private:    //将类的构造设为私有，则无法创建对象. 但可利用友元成员函数CreateObject进行创建
    CStudent();
    CStudent(int no, char *pszname = "student");

    CStudent(const CStudent &stud);

public:
    ~CStudent();

    void KillMe(void);

public:
    void SetName(const char *pszname);
    void SetNo(int no);

    const char *GetName(void) const;
    int GetNo(void) const;

private:
    void init(int no);

private:
    char *m_pszname;
    int m_no;
};


